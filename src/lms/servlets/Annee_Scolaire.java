package lms.servlets;

public class Annee_Scolaire {
	private int id_annee_scolaire;
	private int annee_debut;
	private int annee_fin;
	
	
	public int getId_annee_scolaire() {
		return id_annee_scolaire;
	}
	public void setId_annee_scolaire(int id_annee_scolaire) {
		this.id_annee_scolaire = id_annee_scolaire;
	}
	public int getAnnee_debut() {
		return annee_debut;
	}
	public void setAnnee_debut(int annee_debut) {
		this.annee_debut = annee_debut;
	}
	public int getAnnee_fin() {
		return annee_fin;
	}
	public void setAnnee_fin(int annee_fin) {
		this.annee_fin = annee_fin;
	}
	
	
	
}